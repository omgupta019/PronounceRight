def generate_sql_file():
    sql_filename = "populate_content_library.sql"
    print(f"Generating SQL file '{sql_filename}'...")

    sql_statements = []
    
    # 1. Statement to clear existing records safely
    sql_statements.append("-- 1. Clear existing content in 'content_library'")
    sql_statements.append("DELETE FROM content_library;")
    sql_statements.append("")
    
    # 2. Bulk insert statement
    sql_statements.append("-- 2. Insert premium multi-lingual Indic graded practice dataset")
    sql_statements.append("INSERT INTO content_library (text_content, category, mode, difficulty, language)")
    sql_statements.append("VALUES")

    value_rows = []
    for lang, categories in DATASET.items():
        for category, modes in categories.items():
            for mode, difficulties in modes.items():
                for difficulty, text_content in difficulties.items():
                    # Escape single quotes in SQL strings
                    escaped_text = text_content.replace("'", "''")
                    value_rows.append(f"  ('{escaped_text}', '{category}', '{mode}', '{difficulty}', '{lang}')")

    # Join rows with commas and add final semicolon
    sql_statements.append(",\n".join(value_rows) + ";")
    sql_statements.append("")

    with open(sql_filename, "w", encoding="utf-8") as f:
        f.write("\n".join(sql_statements))

    print(f"\n🚀 SQL File '{sql_filename}' generated successfully! ({len(value_rows)} records included)")
    print("You can easily copy the contents of this file and execute them in your Supabase SQL Editor to instantly populate the database, completely bypassing RLS restrictions!")

if __name__ == "__main__":
    generate_sql_file()