def generate_indic_sql_file():
    sql_filename = "populate_indic_languages.sql"
    print(f"Generating SQL file '{sql_filename}' for other languages (preserving en and hi)...")

    sql_statements = []
    
    # NO DELETE statements are included here to perfectly preserve the existing 888 English and Hindi records!
    sql_statements.append("-- Insert premium multi-lingual Indic graded practice dataset (for other 8 languages)")
    sql_statements.append("INSERT INTO content_library (text_content, category, mode, difficulty, language)")
    sql_statements.append("VALUES")

    value_rows = []
    for lang, categories in INDIC_DATASET.items():
        for category, modes in categories.items():
            for mode, difficulties in modes.items():
                for difficulty, text_content in difficulties.items():
                    # Escape single quotes in SQL strings
                    escaped_text = text_content.replace("'", "''")
                    value_rows.append(f"  ('{escaped_text}', '{category}', '{mode}', '{difficulty}', '{lang}')")

    # Join rows with commas and add final semicolon
    sql_statements.append(",\n".join(value_rows) + ";")
    sql_statements.append("")

    with open(sql_filename, "w", encoding="utf-8") as f:
        f.write("\n".join(sql_statements))

    print(f"\n🚀 SQL File '{sql_filename}' generated successfully! ({len(value_rows)} records included)")
    print("This file ONLY contains records for the other 8 Indic languages (Marathi, Tamil, Telugu, Kannada, Malayalam, Gujarati, Bengali, Punjabi).")
    print("Copying and executing this query in the Supabase SQL editor will append these records while fully preserving your existing 888 English and Hindi records!")

if __name__ == "__main__":
    generate_indic_sql_file()
